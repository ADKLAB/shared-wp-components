 *
 * Атрибуты:
 *   org     — наименование организации-оператора ПД
 *   address — юридический/фактический адрес
 *   email   — e-mail для обращений по ПД
 *   phone   — телефон
 *   site    — URL сайта (для ссылок в тексте)
 *   date    — дата последнего обновления (д.м.гггг), по умолчанию текущая
 *   retention_years — срок хранения данных в годах (по умолчанию 3)
 */

defined('ABSPATH') || exit;

add_shortcode('adklab_privacy_policy', 'adklab_privacy_policy_render');

function adklab_privacy_policy_render(array $atts): string {
    $atts = shortcode_atts([
        'org'             => get_bloginfo('name'),
        'address'         => '',
        'email'           => get_option('admin_email'),
        'phone'           => '',
        'site'            => home_url(),
        'date'            => date('d.m.Y'),
        'retention_years' => '3',
    ], $atts, 'adklab_privacy_policy');

    $org             = esc_html($atts['org']);
    $address         = esc_html($atts['address']);
    $email           = esc_html($atts['email']);
    $email_link      = esc_attr($atts['email']);
    $phone           = esc_html($atts['phone']);
    $site            = esc_url($atts['site']);
    $site_plain      = esc_html($atts['site']);
    $privacy_url     = esc_url(home_url('/privacy'));
    $date            = esc_html($atts['date']);
    $years           = (int) $atts['retention_years'];

    ob_start(); ?>
    <div class="adklab-privacy-policy">

        <p>Настоящая Политика конфиденциальности (далее — «Политика») определяет порядок обработки и защиты персональных данных физических лиц, которые предоставляют свои данные при использовании сайта <strong><?php echo $site_plain; ?></strong>.</p>
        <p>Политика разработана в соответствии с требованиями Федерального закона от 27.07.2006 № 152-ФЗ «О персональных данных».</p>

        <h2>1. Оператор персональных данных</h2>
        <p>
            <strong>Наименование:</strong> <?php echo $org; ?><br>
            <?php if ($address): ?><strong>Адрес:</strong> <?php echo $address; ?><br><?php endif; ?>
            <?php if ($phone): ?><strong>Телефон:</strong> <?php echo $phone; ?><br><?php endif; ?>
            <strong>E-mail:</strong> <a href="mailto:<?php echo $email_link; ?>"><?php echo $email; ?></a>
        </p>

        <h2>2. Категории персональных данных</h2>
        <p>Оператор обрабатывает следующие персональные данные, предоставляемые субъектом добровольно через форму обратной связи:</p>
        <ul>
            <li>Имя (фамилия, имя, отчество — по желанию);</li>
            <li>Номер телефона;</li>
            <li>Адрес электронной почты;</li>
            <li>Наименование компании / организации (по желанию);</li>
            <li>Содержание обращения.</li>
        </ul>

        <h2>3. Цели обработки персональных данных</h2>
        <ul>
            <li>Ответ на запросы и обращения пользователей;</li>
            <li>Консультирование по вопросам ассортимента, цен и условий поставки;</li>
            <li>Оформление и исполнение договорных отношений;</li>
            <li>Направление коммерческих предложений (только с согласия субъекта).</li>
        </ul>

        <h2>4. Правовое основание обработки</h2>
        <ul>
            <li>Согласие субъекта персональных данных (ст. 6, ч. 1, п. 1 ФЗ-152);</li>
            <li>Исполнение договора, стороной которого является субъект (ст. 6, ч. 1, п. 5 ФЗ-152).</li>
        </ul>

        <h2>5. Порядок и условия обработки</h2>
        <ul>
            <li>Обработка осуществляется на законной и справедливой основе;</li>
            <li>Данные не передаются третьим лицам без согласия субъекта, за исключением случаев, предусмотренных законодательством РФ;</li>
            <li>Обработка ограничена достижением конкретных, заранее определённых целей;</li>
            <li>Данные хранятся не дольше, чем этого требуют цели обработки, но не более <strong><?php echo $years; ?> (<?php echo adklab_num_in_words($years); ?>) лет</strong> с момента последнего взаимодействия с субъектом.</li>
        </ul>

        <h2>6. Передача персональных данных третьим лицам</h2>
        <p>Персональные данные не продаются, не передаются и не раскрываются третьим лицам, за исключением:</p>
        <ul>
            <li>Случаев, когда передача необходима для исполнения договора с субъектом (например, транспортным компаниям для доставки);</li>
            <li>Требований уполномоченных государственных органов на основании закона.</li>
        </ul>

        <h2>7. Права субъекта персональных данных</h2>
        <p>Субъект персональных данных имеет право:</p>
        <ul>
            <li>Получить информацию об обработке своих персональных данных (ст. 14 ФЗ-152);</li>
            <li>Требовать уточнения, блокирования или уничтожения данных в случае их неполноты, устаревания или незаконной обработки (ст. 21 ФЗ-152);</li>
            <li>Отозвать согласие на обработку персональных данных в любой момент, направив письменное обращение по адресу Оператора или на e-mail: <a href="mailto:<?php echo $email_link; ?>"><?php echo $email; ?></a>;</li>
            <li>Обжаловать действия Оператора в Роскомнадзоре или в суде.</li>
        </ul>
        <p>При отзыве согласия Оператор прекращает обработку данных и уничтожает их в срок не позднее <strong>30 дней</strong>, если иное не предусмотрено законодательством.</p>

        <h2>8. Защита персональных данных</h2>
        <ul>
            <li>Ограничение доступа к данным для сотрудников;</li>
            <li>Использование защищённого соединения (HTTPS) на сайте;</li>
            <li>Регулярный контроль соответствия мер защиты требованиям законодательства.</li>
        </ul>

        <h2>9. Использование файлов cookie</h2>
        <p>Сайт использует файлы cookie для корректной работы корзины, авторизации и аналитики. Cookie не содержат личных данных в явном виде. Пользователь может отключить cookie в настройках браузера, однако это может повлиять на функциональность сайта.</p>

        <h2>10. Актуальность политики</h2>
        <p>Настоящая Политика может быть изменена Оператором без предварительного уведомления. Актуальная версия всегда доступна на странице: <a href="<?php echo $privacy_url; ?>"><?php echo $privacy_url; ?></a></p>
        <p><strong>Дата последнего обновления:</strong> <?php echo $date; ?></p>

    </div>

    <style>
    .adklab-privacy-policy { max-width: 800px; line-height: 1.7; color: #1a1a2e; }
    .adklab-privacy-policy h2 { margin: 2em 0 0.5em; font-size: 1.1rem; color: #1565c0; }
    .adklab-privacy-policy ul { padding-left: 1.5em; margin: 0.5em 0; }
    .adklab-privacy-policy li { margin-bottom: 0.4em; }
    .adklab-privacy-policy a { color: #1565c0; }
    .adklab-privacy-policy p { margin: 0.6em 0; }
    </style>
    <?php
    return ob_get_clean();
}

function adklab_num_in_words(int $n): string {
    $words = [1 => 'одного', 2 => 'двух', 3 => 'трёх', 4 => 'четырёх', 5 => 'пяти'];
    return $words[$n] ?? (string) $n;
}
